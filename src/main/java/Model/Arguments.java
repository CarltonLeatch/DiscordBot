package main.java.Model;

public class Arguments {

    private int id;
    private String Argument;
    private int count;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getArgument() {
        return Argument;
    }

    public void setArgument(String argument) {
        Argument = argument;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
